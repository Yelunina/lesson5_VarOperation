public class OperationsAppl {
    public static void main(String[] args) {
        int x = 20;
        int y = 10;
        int z = x + y;
        System.out.println(z);
        z = x - y;
        System.out.println(z);
        z = x * y;
        System.out.println(z);
        z = x / y;
        System.out.println(z);
        double a = 1.0 * y / x;
        System.out.println(a);
        a = y / (x * 1.);
        System.out.println(a);
        a = (x + y) / 10;
        System.out.println(a);
        z = 18 % 7;
        System.out.println(z);
        a = 18. / 7;
        System.out.println(a);

    }
}
